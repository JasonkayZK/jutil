package top.jasonkayzk.jutil.enums;

/**
 * 日志级别枚举类
 *
 * @author zk
 */
public enum LogLevel {

    /**
     * 普通消息
     *
     * @since 1.1.0
     */
    INFO,

    /**
     * 警告
     *
     * @since 1.1.0
     */
    WARN,

    /**
     * 错误
     *
     * @since 1.1.0
     */
    ERROR,

    /**
     * 严重错误
     *
     * @since 1.1.0
     */
    FATAL
}
